export const seedProducts = async () => {
  try {
    const count = await Product.countDocuments();
    if (count === 0) {
      console.log(`[SEED] Product catalog in database is empty. Seeding ${seedProductsData.length} designs...`);
      await Product.insertMany(seedProductsData);
      console.log(`[SEED] Seeded product designs successfully into MongoDB!`);
    } else {
      console.log(`[SEED] Products already exist in database (${count} designs found). Skipping seeder.`);
    }
  } catch (err) {
    console.error(`[SEED] Error seeding product catalog:`, err);
  }
};
